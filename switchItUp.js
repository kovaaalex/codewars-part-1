function switchItUp(number){
    switch (number) {
        case number = 0:
            return "Zero"
            
        case number = 1:
            return "One"
            
        case number = 2:
            return "Two"
            
        case number = 3:
            return "Three"
            
        case number = 4:
            return "Four"
            
        case number = 5:
            return "Five"
            
        case number = 6:
            return "Six"

        case number = 7:
            return "Seven"
            
        case number = 8:
            return "Eight"
            
        case number = 9:
            return "Nine"
    }
}